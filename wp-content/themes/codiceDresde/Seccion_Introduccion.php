<p>El <span class="cursivatxt">Códice de Dresde</span> es uno de los tres manuscritos jeroglíficos mayas cuya autenticidad nadie ha puesto en duda. Los otros dos son los códices de <span class="cursivatxt">Madrid</span> y de <span class="cursivatxt">París</span>. Se trata de un libro plegado de papel amate (<span class="cursivatxt">Ficus cotinifolia</span>), aunque cada hoja está cubierta con una capa delgada de cal o estuco que servía para darles tersura. En los textos jeroglíficos mayas el nombre de dicho tipo de papel, de los códices mismos y de la deidad que habitaba en el interior de tales objetos era <span class="cursivatxt">hu’un</span>.</p>

<p class="sangria">El <span class="cursivatxt">hu’un</span> o códice que nos ocupa tiene 39 hojas, la mayoría de las cuales están escritas y pintadas por ambos lados. Cada hoja tiene 20.4 centímetros de alto por 9 de ancho, dando un total de 3.50 metros de largo cuando está extendido. Actualmente se resguarda la Biblioteca del Estado de Sajonia, en la ciudad de Dresde, Alemania.</p>



<p><h2>Contenido general del códice </h2></p>


<p>El<span class="cursivatxt"> Códice de Dresde </span>es una compilación de complejos  almanaques adivinatorios<span class="cursivatxt">,</span> tablas astronómicas, calendáricas y numéricas, cuyo fin último es la pronosticación del futuro en el marco de un orden sagrado que fue instituido desde los tiempos míticos arcanos, sin dejar de lado importante información ritual. El fundamento tanto de los augurios como de las ceremonias religiosas son los mitos cosmogónicos. Sus contenidos son variados, pues incluye vaticinios para diversos aspectos de la vida cotidiana de los campesinos, para los ciclos de Venus, de la Luna y de los eclipses, para los <span class="cursivatxt">k’atuunes </span>o periodos de 20 años de 360 días cada uno, para la lluvia, la sequía, los ritos de año nuevo, la ceremonias agrícolas de los “quemadores” y posiblemente para los movimientos del planeta Marte, proporcionando información valiosa sobre los mitos y los atributos de los dioses, mucha de la cual no se encuentra en otros documentos mayas.</p>



<p class="sangria">Las secciones o “capítulos” en los que está dividido el códice casi siempre pertenecen a dos categorías diferentes: los <span class="cursivatxt">almanaques</span> y las <span class="cursivatxt">tablas</span>. Los primeros consisten en iconotextos (<span class="cursivatxt">t’o’ol</span>) donde cada frase e imagen corresponde a determinadas fechas del <span class="cursivatxt">tzolk’iin</span> o calendario adivinatorio de 260 días. La mayoría de los <span class="cursivatxt">almanaques</span> corresponden a un solo ciclo de 260 días, pero existen algunos que abarcan dos (520 días), tres (780 días) o más de esos ciclos. Puesto que el <span class="cursivatxt">tzolk’iin</span> es una cuenta cíclica que se repite cada 260 días, la idea que prevalece entre los mayistas es que los almanaques no se pueden ubicar en el tiempo lineal, pues se trata de instrumentos predictivos que pueden servir en cualquier momento histórico. No obstante, los esposos Bricker (2011) han intentado sostener que no es así.</p>



<p class="sangria">Por lo que se refiere a las <span class="cursivatxt">tablas</span>, éstas sí contienen fechas de Cuenta Larga que corresponden a posiciones específicas de tiempos histórico o lineal, según lo concebimos los hombres de la cultura occidental. Cada tabla contiene un “prefacio”, que arranca con un número de anillo, mismo que debe restarse a la <span class="cursivatxt">fecha era</span> o de creación del mundo según los mayas: 13 de agosto de 3114 a.C. en el calendario gregoriano. A través de dicha sustracción se alcanza una fecha del pasado mítico, conocida como <span class="cursivatxt">fecha base de la tabla</span>, a la que casi siempre corresponde un texto jeroglífico de carácter mítico, que al mismo tiempo sintetiza temas que más adelante se detallarán en la <span class="cursivatxt">tabla</span>. Un número de distancia negro, expresado a través del sistema de la Cuenta Larga, conduce desde esa <span class="cursivatxt">fecha base</span> de carácter mítico, hasta algún momento del periodo Clásico maya (300-900 d.C.) cuyo día del <span class="cursivatxt">tzolk’iin</span> es idéntico al de la <span class="cursivatxt">fecha mítica base</span>. Luego de semejante “prefacio” cada <span class="cursivatxt">tabla</span> contiene una serie de múltiplos que arrancan desde la <span class="cursivatxt">fecha base</span>, mismos que por lo general ascienden de derecha a izquierda y de abajo hacia arriba. Algunas veces esta sección de múltiplos contiene sugerencias numéricas para corregir la tabla, recuperando la precisión astronómica en la misma <span class="cursivatxt">fecha sagrada </span>o<span class="cursivatxt"> base</span> del <span class="cursivatxt">tzolk’iin</span>. Entre los propósitos evidentes de esos múltiplos está el de poder reutilizar la <span class="cursivatxt">tabla </span>en distintos momentos de la historia. Luego del “prefacio” y de esta sección de múltiplos, la tercera parte de las tablas ya corresponde a los augurios o pronósticos en fechas específicas del <span class="cursivatxt">tzolk’iin</span>, pero conmensurando este último ciclo con el del astro o fenómeno celeste que es el tema de la tabla. Muchas veces estos ciclos también se conmensuran con el año vago de 365 días. En esta tercera sección de las <span class="cursivatxt">tablas</span> suelen aparecer viñetas iconográficas que generalmente corresponden a cada texto jeroglífico, dando lugar a verdaderos <span class="cursivatxt">t’o’ol </span>o iconotextos.</p>



<p class="sangria">Es preciso mencionar que aunque la mayoría de los “capítulos” del códice son <span class="cursivatxt">almanaques</span> o <span class="cursivatxt">tablas</span>, existen secciones especiales que no encajan <span class="cursivatxt">sensu stricto </span>con ninguna de estas dos categorías, como por ejemplo las páginas de año nuevo (pp. 25-28 del códice), que contienen información ritual que pertinente en los días del año viejo y nuevo que se celebraban cada 365 días. No debemos perder de vista que a cada rito corresponde un pronóstico o augurio, y que el fundamento de los ritos siempre son los mitos cosmogónicos.</p>


<p class="sangria">La brevedad del espacio exige sacrificar algunos aspectos del códice a lo largo del comentario. Por ejemplo, a cada uno los textos augurales de los almanaques les corresponden por lo menos cuatro o cinco fechas posibles, pero en esta edición preferimos darle importancia a la lectura fonética del texto jeroglífico, tomando en cuenta los avances de la epigrafía, en vez de detallar los elementos calendáricos, que el lector podrá consultar por ejemplo en el comentario de Thompson (1972; 1988). Lo anterior obedece a que la interpretación de los ingredientes cronológicos no ha cambiado radicalmente desde hace décadas; no así las lecturas fonéticas de los jeroglíficos, que siempre es necesario actualizar.</p>

<p class="sangria">Tomemos por ejemplo el almanaque que aparece en la página 19b. En vez de decir que al enunciado <span class="cursivatxt">yat[aa]l Ixik Uh, Sak Ixik, ox wi’[il]</span>, el pago de Ixik Uh, Sak Ixik, es mucha comida’, le corresponden indistintamente las fechas &lt;<span class="cursivatxt"> <i>10</i> ik, </span><span class="cursivatxt"><i>10</i> hix, </span><span class="cursivatxt"><i>10</i> cimi, </span><span class="cursivatxt"><i>10</i> edznab</span>&gt; y &lt;<span class="cursivatxt"><i>10</i> oc</span>&gt;, y que 29 días después tenemos frase <span class="cursivatxt">yat[aa]l Ixik Uh, Sak Ixik, cham[a]l</span>, ‘el pago de Ixik Uh, Sak Ixik, es mortandad’, a la que le corresponden las posibles fechas &lt;<span class="cursivatxt"><i>13</i> chuen, </span><span class="cursivatxt"><i>13</i> akbal, </span><span class="cursivatxt"><i>13</i> men, </span><span class="cursivatxt"><i>13</i> manik</span>&gt; y &lt;<span class="cursivatxt"><i>13</i> cauac</span>&gt;, sólo describo por falta de espacio los signos de la primera columna de días y señalo de forma general que existen determinados números de distancia de color negro que conducen a fechas con coeficientes de tales números rojos.</p>

<p class="sangria">Tampoco detalla con exhaustividad los ricos elementos de la escena. Ello se debe a la falta de espacio. Tan sólo me detengo sobre esos puntos cuando así lo considere pertinente. En lo que atañe a las lecturas de los jeroglíficos tan sólo proporcionaré la transcripción (con <span class="cursivatxt">cursivas</span>) y no la transliteración (con <strong>negrillas</strong>). Esto es, intentando“representar de una forma lo más apropiada posible la pronunciación del original”, en vez de preocuparme porque “exista una correspondencia de ‘uno a uno’ entre los signos o grupos de signos utilizados”. Por ejemplo, [<strong>bu</strong>]-<strong>lu-ku</strong> representa la transliteración, que rara vez voy a hacer en este comentario. Pero <span class="cursivatxt">buluk</span> sería la trancripción. Su traducción es‘once’. Los corchetes [] siempre representan elementos restituidos por mí, que no están escritos en original, o que se han perdido. Mientras que las palabras que escriba entre corchetes angulares &lt;&gt; significa que están en ortografía tradicional o de la época colonial. Prefería dejar en ortografía virreinal los nombres de los días del calendario, a causa de que no sabemos aún con precisión cómo debemos modernizar las vocales. Cuando a una lectura o traducción le agregue un signo de interrogación entre paréntesis, por ejemplo <span class="cursivatxt">Ahan</span>(?), significa que es una opción viable de acuerdo con nuestros datos actuales, pero su desciframiento no está plenamente comprobado.</p>

<p class="sangria">En este trabajo <span class="cursivatxt">grafema</span>, <span class="cursivatxt">grafía</span>, <span class="cursivatxt">jeroglífico</span> o <span class="cursivatxt">signo</span> se usan como sinónimos entre sí. Denotan la unidad mínima del sistema de escritura maya (lo que muchos autores llaman <span class="cursivatxt">glifos</span>). Éstas unidades mínimas pueden ser principalmente de dos clases: <span class="cursivatxt">logogramas</span>: cuando representan una palabra completa, por ejemplo <strong>AJAW</strong>, ‘señor’; o <span class="cursivatxt">silabogramas</span>, cuando sólo expresan sonidos silábicos sin significado, por ejemplo <strong>pe</strong>. <span class="cursivatxt">Alógrafo</span> significa la <span class="cursivatxt">variante gráfica</span> de un mismo signo, como sucede con nuestra “A” y con nuestra “a”, que tienen el mismo valor de lectura entre ellas, a pesar de ser grafías muy diferentes. De vez en cuando nombro a los grafemas por medio de las claves alfanuméricas del catálogo de jeroglíficos mayas de Thompson (1962): por ejemplo el T510 (logograma <strong>EK’</strong>, ‘estrella’) o el T1 (silabograma <strong>u</strong>).</p>



