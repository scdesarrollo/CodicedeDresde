	<p> 
		El siglo XIX trastocó en muchos sentidos al mundo entero. Fue una época de

revoluciones políticas e industriales, donde los cansados imperios europeos se

desmoronaban frente a los férreos partidarios de las repúblicas que, con miras a

un futuro alentador, luchaban por construir la cimentación que habría de soportar

su ideología política y económica. Pero otros hubo que paradójicamente buscaron

ese futuro escarbando en el pasado en un intento de comprender a las antiguas

civilizaciones que les precedieron en tiempo y espacio. Así fue como la Hélade,

Egipto y los países árabes se convirtieron en los lugares más visitados por

turistas, aventureros, arqueólogos y exploradores; cuyas andanzas y

descubrimientos nos legaron en forma de diarios de viajero, de informes

arqueológicos y un sinfín de “vistas”, es decir: dibujos, grabados, litografías o

fotografías que inmortalizaron las milenarias ruinas que visitaban.
	</p>


<p class="sangria">
Esta fiebre de descubrimientos alcanzó también a las culturas

prehispánicas desarrolladas en Mesoamérica, especialmente a las de habla

náhuatl y maya. Desde el siglo XVIII ya existían algunos acercamientos hacia las primeras,

como los realizados por Lorenzo Boturini Benaduci en su <span class="cursivatxt">Idea de una nueva

historia de América Septentrional</span>, Francisco Xavier Clavijero con su Historia

Antigua de México y por Antonio de León y Gama en la <span class="cursivatxt">Descripción histórica

y cronológica de las dos piedras</span>; tres preclaros intentos de comprensión del pasado

indígena mexicano. Por el contrario, el siglo XIX fue la época de los grandes

descubrimientos mayas. Al igual que como pasó en el desierto de Egipto, viajeros

y exploradores de distintas partes del mundo se sintieron atraídos por los vestigios

arqueológicos localizados en el enigmático sureste mexicano y en Centroamérica.

La cultura maya renacía ante el mundo. Grandes figuras como Frédérick Waldeck,

Frederick Catherwood y Désiré de Charnay seguramente quedaron deslumbrados

cuando la selva se abrió y permitió admirar los templos, monumentos y estelas

erigidos en lugares como Palenque, Yaxchilan o Tikal. Quizá la misma sorpresa

inundó a Charles Étienne Brasseur de Bourbourg, cuando ante él cobraron vida

las voces dormidas que aguardaban en los antiguos manuscritos mayas que él

mismo descubrió.
</p>


<p class="sangria">
	Un considerable número de esas construcciones, vasijas y algunos otros

objetos de uso cotidiano, estaban revestidos de extrañas pinturas e inscripciones,

que no pocos interpretaron como la “prueba definitiva” de un supuesto contacto

entre los focos civilizatorios del viejo continente con los pueblos mesoamericanos.

Con el tiempo, finalmente se descubrió que aquellas inscripciones eran el más

complejo sistema de escritura que se hubiera desarrollado en América. A través

de él los mayas habían registrado algunos hechos históricos y proezas realizadas

por sus gobernantes, así como un complejo sistema de creencias que regía todos

los ámbitos de su vida cotidiana.
</p>

<p class="sangria">

Esta antigua sociedad no solo utilizó la dureza de las piedras para

comunicar sus intereses religiosos y políticos; también conoció el arte de “escribir

pintando”. Los libros o códices mayas fueron confeccionados por los <span class="cursivatxt">ah tz’ ib</span> o

escribanos mayas; quienes se valieron de largas tiras de amate que plegaban una

tras otra a manera de biombo y que luego cubrían con una fina capa de cal,

creando así una superficie adecuada para registrar con colores negro, rojo, azul,

amarillo y ocre diversos temas de su interés y necesidad. Lamentablemente

ninguno de los códices que nos llegaron contiene información de índole histórica o

económica, aunque no hay duda que hubo documentos con ambos tópicos.

</p>

<p class="sangria">
	
	Tres son los códices mayas que pervivieron hasta nuestros días sorteando

todo tipo de avatares, como las extremas condiciones climáticas propias del

sureste mexicano o el desatinado auto de fe promovido por el fraile franciscano

Diego de Landa en 1562, donde quemó una buena cantidad de estos manuscritos.

Dichos códices fueron descubiertos en distintos momentos a lo largo del siglo

XVIII y en la actualidad son conocidos por el nombre de la ciudad que los

resguarda: <span class="cursivatxt">Dresde, Paris y Madrid</span>; cabe señalar que los tres son considerados los

documentos más antiguos que sobrevivieron al periodo prehispánico. Un cuarto

códice se dio a conocer en 1971 y fue identificado bajo el nombre de <span class="cursivatxt">Grolier</span>, en

honor al club del mismo nombre que organizó la exposición donde el documento

fue presentado al mundo, momento a partir del cual se ha visto envuelto en

controversias sobre su autenticidad.
</p>



<p class="sangria">
	De los cuatro, es el <span class="cursivatxt">Códice de Dresde</span> el que ha recibido mayor atención por

parte de la comunidad académica, ya que es considerado una pieza clave en el

desciframiento de la escritura maya y en la correlación de su calendario con

respecto al calendario gregoriano. Hasta el momento no se ha logrado establecer

su fecha de manufactura, pero algunos investigadores consideran que fue

elaborado aproximadamente en el año 1250 d. C., durante el Posclásico

temprano. Fue confeccionado en una larga tira de amate plegada a manera de

biombo, la cual está dividida en 39 láminas de 20.4 x 9 cm, pintadas en su

mayoría por ambos lados.

</p>

<p class="sangria">
	Pocas también son las noticias históricas sobre este códice. Sabemos que

en 1739 fue entregado como un obsequio a Johann Christian Götze, director de la

Biblioteca Real Pública de Dresde, quien decidió donarlo a la institución. En los

años siguientes el códice fue consultado por importantes personajes como el

barón Alexander von Humboldt, quien en 1813 reprodujo cinco de sus láminas

dentro de su famoso libro <span class="cursivatxt"> Vues des Cordilléres et monumens des peuples

indigénes de l’ Amerique</span> y Edward King Lord Kingsborough quien lo publicó

íntegramente en su monumental <span class="cursivatxt"> Antiquities of Mexico</span>. Al ser un documento

sumamente demandado, en 1836 Karl C. Falkenstein, quien a la sazón se

desempeñaba como director de la biblioteca de Dresde, decidió colocar el códice

entre dos cristales con la finalidad de facilitar su consulta y protegerlo de la

intemperie, aunque al hacerlo desafortunadamente desfasó la secuencia de sus

folios.

</p>


<p class="sangria"> 
Cuando Ernst W. Förstemann ingresó como director a la Biblioteca de

Dresde en 1865, pronto mostró gran interés por el manuscrito maya dedicándole

numerosas horas de su tiempo. Casi tres décadas más tarde, en 1892, su arduo

trabajo rindió frutos al publicar la que sería hasta el momento la mejor

reproducción cromolitográfica del códice, la cual acompañó con una explicación

sobre la forma en que funcionaba el calendario maya y su cuenta larga, así como

distintos aspectos astronómicos que logró identificar. Lo anterior significó un

parteaguas que revolucionó los incipientes estudios que hasta entonces se habían

realizado sobre la escritura maya.
</p>


<p class="sangria"> 
	Entre 1939 y 1945 el destino marcó para siempre a nuestro documento.

Durante esos años Europa se vio inmersa en un acelerado baile de sangre, donde

las grandes potencias mundiales midieron su poder enfrascándose en una terrible

Segunda Guerra Mundial. Durante ese sombrío periodo en la historia de la

humanidad se sacrificaron no solo numerosas vidas, sino también buena parte del

patrimonio histórico y cultural, que durante siglos había permanecido incólume. Al

igual que otras ciudades del viejo continente, Dresde resultó seriamente lastimada

por los constantes bombardeos que día y noche la asolaron. El edificio que

resguardaba su biblioteca no fue la excepción y sus instalaciones se vieron

afectadas por detonaciones que provocaron constantes escurrimientos de agua

que dañaron considerablemente sus acervos. Lamentablemente el Códice de

Dresde no resultó ileso y debido a la humedad perdió en algunas partes su

información, razón por la cual poco tiempo después fue trasladado a la Biblioteca

de Sajonia donde es resguardado actualmente.
</p>

<p class="sangria">
	
	Por lo que respecta al contenido, las pictografías plasmadas en el Códice

de Dresde –así como las de los otros dos códices de la región- son una muestra o

ejemplo de los intereses y necesidades de los sacerdotes mayas: la religión, la

astronomía y el tiempo. En ese sentido y según la opinión del reconocido

epigrafista Erick Velásquez este códice es [...] <span class="cursivatxt">una compilación de complejos

almanaques adivinatorios, tablas astronómicas, calendáricas y numéricas , cuyo

fin último es la pronosticación del futuro en el marco de un orden sagrado,

instituido desde los tiempos míticos arcanos, sin dejar de lado importante

información ritual [...] además proporciona información valiosa sobre los mitos y

los atributos de los dioses</span>  [...]  Así de variado e interesante es el contenido de este

antiguo libro mesoamericano.
</p>


<p class="sangria">
	
	Como es bien sabido, la materialidad de los códices mexicanos es

sumamente susceptible a las inclemencias del tiempo, razón por la que muchos de

ellos se encuentran bajo el resguardo de instituciones académicas especializadas,

donde por conservación se restringe su acceso únicamente a especialistas en la

materia. Lo anterior impide que estudiantes o el público en general aprecien tan

importante patrimonio histórico. Si a esto agregamos el hecho de que muchos de

ellos se encuentran en el extranjero, resulta prácticamente imposible observar de

cerca este tipo de manuscritos. Ante este panorama el Instituto Nacional de

