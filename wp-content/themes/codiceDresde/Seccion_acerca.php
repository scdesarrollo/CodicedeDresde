		<p>El Instituto Nacional de Antropología e Historia pone a disposición de los interesados una novedosa edición física y digital de uno de los documentos históricos más relevantes y antiguos del México prehispánico: <i>El Códice de Dresde</i>. Con ello continuamos la directriz tecnológica marcada por el Instituto desde los años 2014 y 2015 con la publicación digital del <i>Códice Mendoza</i> y del <i>Códice Boturini</i>; importantes trabajos en los que hemos conjuntado las nuevas tecnologías y los antiguos soportes, con la finalidad de ampliar y difundir gratuita y democráticamente el conocimiento que subyace en los antiguos códices mexicanos.
		</span>
		</p>

		 <p class="sangria">La utilización de un componente físico y uno digital de manera simultánea, ha demostrado ser la fórmula idónea para representar objetos culturales sumamente dinámicos como los códices mexicanos; los cuales están compuestos por diferentes lenguajes como el escrito y el pictórico; a lo cual podemos añadir digitalmente un contexto amplio que nos permita entender su significado y  simplificar la comprensión del documento volviéndolo más transparente y asequible con respecto a una edición impresa tradicional. Lo anterior también ha permitido una pertinente discusión sobre la forma de representación de estos manuscritos.  
		</p>

		<p class="sangria">
		Sin lugar a dudas estas publicaciones han posicionado al Instituto en una palestra desde la cual ha logrado incentivar a instituciones e investigadores nacionales y extranjeros a explorar, compartir, reflexionar y debatir en torno al uso de las nuevas tecnologías en el estudio y difusión del patrimonio histórico y cultural de nuestro país y del mundo. Lo anterior no solo beneficia el aspecto académico e institucional, sino que también permite que México estreche y fortalezca vínculos con otras naciones. Así sucedió con Alemania, el país que resguarda actualmente el Códice de Dresde y el cual, a través de su embajada en México y para celebrar el Año Dual México-Alemania, ha proporcionado al INAH todas las facilidades para llevar a buen puerto la publicación de este extraordinario códice. </p>       

		<p class="sangria">
		Los dos componentes, el digital y el impreso de la nueva edición del Códice de Dresde están integrados de la siguiente manera: 
		</p>


		<p><h2>1. Edición digital. </h2></p>

		<p><i>Reconstrucción Virtual</i>. A finales de la Segunda Guerra Mundial la biblioteca que resguardaba el códice en la ciudad de Dresde fue alcanzada por una bomba, lo que ocasionó la inundación del recinto y el consiguiente deterioro del Códice de Dresde. Afortunadamente, varias décadas atrás el bibliotecario Ernst Förstemann había reproducido, con sumo detalle y mediante la técnica cromolitográfica, el códice completo en una edición que actualmente es muy difícil de conseguir. A través de la tecnología utilizada en esta nueva edición digital es posible cotejar el códice en su estado actual, con la versión facsimilar realizada por Förstemann y conocer así como era el original antes de su deterioro.  </p>
 

		 <p><i>Hypermedia</i>. Es una funcionalidad interactiva que funciona a través de una interface intuitiva que permite conocer el significado de dos de las secciones principales del códice: “Dioses” y “Augurios”. Cuyo contenido está fundamentado en el trabajo académico del doctor Erik Velázquez García.  </p>
 
 		<p><i>Sistemas cronográficos</i>. Mediante un algoritmo desarrollado por el investigador del INAH Stansilaw Iwaniszewski, especialista en 	arqueoastronomía maya, se ha desarrollado una interfaz que permite al usuario interactuar con el calendario maya para trasladar cualquier fecha de 	éste al sistema gregoriano.  </p>
 
		<p><i>Anotaciones.</i> Esta funcionalidad permite apuntar las diversas secciones del códice para crear un entorno donde los especialistas y el público interesado puedan intercambiar información relacionada con el documento. </p>

		<p><h2>2. Edición facsimilar impresa </h2></p>
		<p>La edición facsimilar impresa reproduce no solo las imágenes del códice, sino también su materialidad en amate. Para lograr lo anterior, se encomendó la manufactura del papel a los artesanos otomíes de la comunidad de San Pablito Pahuatlán en el estado de Puebla, quienes con destreza y dedicación elaboraron los pliegos de gran formato donde fue reproducido el códice utilizando tintas de alta durabilidad. </p>


		<p><h2> Diseño </h2></p>

		<p>La experiencia creada en el trabajo de las ediciones del Códice Mendoza y el Códice Boturini nos ha permitido reconocer hacia dónde encaminar las líneas gráficas y de diseño en este proyecto. En todos los casos se ha buscado que la interfaz sea lo más sencilla para permitir una mayor transparencia en la representación del original. En el caso del Códice de Dresde, dada la complejidad conceptual que implica la lectura de los códices mayas, se procuró dotar de más herramientas de navegación, sin por eso afectar la mencionada sencillez en la interfaz gráfica. En este punto es importante destacar que las imágenes en alta resolución del Códice de Dresde fueron proporcionadas por la Biblioteca del Estado de Sajonia (<i>Sächsische Landesbibliothek – Staats- und Universitätsbibliothek Dresden- SLUB Dresden</i>), repositorio que resguarda el códice original.</p>

		<p><h2> Desarrollo Tecnológico </h2></p>

		<p>La versión digital del Códice de Dresde implicó la interacción de diversos lenguajes de programación para comunicar todos estos valores al usuario. En la visualización de las imágenes en alta calidad se utilizaron estándares de procesamiento como el DZI (Deep Zoom Image) de Microsoft, lo que permite la navegación de las diferentes versiones y caras del códice sin sacrificar rendimiento en dispositivos de escritorio o móviles. La base de datos se construyó utilizando MySQL y se aplicó CS3, mientras que para los estilos se implementó la tecnología Java Script, Jquery y la API de WordPress (PHP) para la administración del contenido de todas las secciones. Uno de los elementos innovadores dentro de esta edición, es la integración del algoritmo desarrollado por el doctor Stanislaw Iwaniszewski que permitió generar una herramienta que con base en el calendario maya puede calcular fechas en la cuenta larga maya (<i>kin, uinal, tun, katún y baktún</i>) y en el calendario gregoriano (365 días); dicha herramienta demuestra la potencia matemática de los antiguos mayas, al mismo tiempo que permite un acercamiento lúdico a un documento tan complejo como lo es el Códice de Dresde.</p>

		 <p class="sangria">
			Una vez vistos los elementos técnicos de la edición, no queda más que subrayar que en este proyecto y en los anteriores, el Instituto ha procurado innovar en aspectos tecnológicos, de interacción y de diseño; con la finalidad de renovar la forma en la que investigamos, preservamos y difundimos el patrimonio nacional. Lo anterior se ha convertido en una de las principales premisas de la Secretaría de Cultura y del Instituto Nacional de Antropología e Historia. La presente edición del Códice de Dresde constituye, sin lugar a dudas, un peldaño más en este cambio paradigmático al interior de las instituciones mexicanas de cultura, y al mismo tiempo nos acerca cada vez más a un tratamiento original y novedoso del patrimonio cultural que consolide su acceso universal a los ciudadanos del siglo XXI. 

		</p>  

